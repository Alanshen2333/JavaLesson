    public class ForSentence
    {
        public static void main(String[] args)
        {
            int s=0;
            for (int i = 0;i <100;i++) //声明一个变量 i，i 从 1 到 99，每次递增 1
            {
                s +=i;
            }
            System.out.println("1+3+5+…+99="+s);
        }
    }

